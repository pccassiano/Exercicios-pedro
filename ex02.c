/*
calcula o cubo de um numero
*/

#include<stdio.h>

int main(){
    int numero, resultado;

    numero = 4;
    resultado = numero * numero * numero;
    printf("O cubo de %d eh igual a %d.\n\n", numero, resultado);

    numero = 5;
    resultado = numero * numero * numero;
    printf("%d ^ 3 = %d.\n\n", numero, resultado);

    return 3;
}







